

let ham = document.querySelector('.hamburger');

let container = document.querySelector('.mobile-menu-container');
let close = document.querySelector('.btn-close');

ham.addEventListener('click' , ()=>{

    container.style.display ="block";

})

close.addEventListener('click',()=>{

    container.style.display='none';
})




 


// for Meet 

let details= document.querySelector('.meet-box');
let quote = document.querySelector('.meet-quote');
let expandClose = document.querySelector('.expand-close')

details.addEventListener('click', ()=>{
    quote.style.display='block';
    details.style.display='none';

})

expandClose.addEventListener('click', ()=>{
    quote.style.display='none';
    details.style.display='flex';
})

